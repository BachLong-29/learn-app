import { Button, Modal } from 'antd';

import { $FixType } from 'utils/constants';
import { Flex } from 'ui-components/General/Flex';
import Head from 'next/head';
import NegativeButton from 'ui-components/Button/NegativeButton';
import PositiveButton from 'ui-components/Button/PositiveButton';
import RightOutlined from '@ant-design/icons/RightOutlined';
import SVGIcon from 'components/SVGIcon';
import StudentForm from '../components/StudentForm';
import { addStudentRedux } from 'redux/actions/student.action';
import defaultTheme from 'ui-components/theme/theme';
import studentApi from 'pages/api/studentService';
import styled from 'styled-components';
import { useCallback } from 'react';
import { useDispatch } from 'react-redux';
import { useRouter } from 'next/router';
import { v4 as uuid } from 'uuid';

export default function User() {
  const dispatch = useDispatch();
  const router = useRouter();
  const handleSubmit = (data: $FixType) => {
    const id = uuid();
    const createData = {
      id: id,
      name: data.name,
      age: data.age,
      gender: data.gender,
      nickname: data.nickname,
      rank: data.rank,
      images: data.images,
    };
    studentApi
      .createStudent(createData)
      .then(() => {
        dispatch(addStudentRedux(createData));
        Modal.success({
          style: { padding: 0 },
          className: 'successModal',
          title: (
            <>
              <SVGIcon
                src="/images/svg/success-icon.svg"
                size={60}
                s={{
                  position: 'absolute',
                  top: '-32px',
                  left: 'calc(50% - 30px)',
                }}
              />
            </>
          ),
          maskStyle: { padding: 0 },
          content: (
            <Flex
              justifyContent="center"
              alignItems="center"
              h={200}
              w={270}
              fontSize="32px"
              color={defaultTheme.colors.dark_blue}
            >
              Success!
            </Flex>
          ),
          width: 270,
          centered: true,
          maskClosable: true,
          footer: (
            <Flex
              background="#8ddaaa"
              h={100}
              style={{
                borderBottomLeftRadius: '8px',
                borderBottomRightRadius: '8px',
              }}
              justifyContent="center"
              alignItems="center"
            >
              <Button
                style={{
                  padding: '10px',
                  width: '150px',
                  height: '50px',
                  borderRadius: '40px',
                }}
                onClick={() => {
                  router.push('/student');
                }}
              >
                Go List
                <RightOutlined style={{ fontSize: '12px' }} />
              </Button>
            </Flex>
          ),
          icon: null,
        });
      })
      .catch((error) => {
        console.log({ error });
        Modal.error({
          title: 'Error',
          content: 'error',
        });
      });
  };
  const renderAction = useCallback((form: any) => {
    return (
      <FormFooter>
        <NegativeButton
          onClick={() => {
            router.push('/student');
          }}
        >
          Cancel
        </NegativeButton>
        <PositiveButton onClick={() => handleSubmit(form?.values)}>
          Save
        </PositiveButton>
      </FormFooter>
    );
  }, []);

  return (
    <div>
      <Head>
        <title>Create New Student</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <StudentForm renderAction={renderAction} />
    </div>
  );
}

const FormFooter = styled(Flex)`
  background: ${defaultTheme.colors.white};
  border-end-end-radius: 4px;
  justify-content: center;
  align-items: center;
  height: 100px;
  gap: 40px;
`;
